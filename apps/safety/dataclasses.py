@dataclass
class ReversibleActionData:
    """
    Data for a reversible action that can be undone.
    
    Requirements: 12.6
    
    Attributes:
        action_id: Unique identifier for the action
        user_id: The user who owns this action
        action_type: The type of action performed
        target_integration: The integration the action was performed on
        original_state: The state before the action (for rollback)
        new_state: The state after the action
        undo_deadline: Deadline for undoing the action
        is_undone: Whether the action has been undone
        created_at: When the action was performed
    """
    
    action_id: str
    user_id: str
    action_type: str
    target_integration: Optional[str]
    original_state: dict
    new_state: dict
    undo_deadline: datetime
    is_undone: bool
    created_at: datetime
    
    @classmethod
    def from_model(cls, reversible_action) -> 'ReversibleActionData':
        """Create from a ReversibleAction model instance."""
        return cls(
            action_id=str(reversible_action.id),
            user_id=str(reversible_action.user_id),
            action_type=reversible_action.action_type,
            target_integration=reversible_action.target_integration,
            original_state=reversible_action.original_state,
            new_state=reversible_action.new_state,
            undo_deadline=reversible_action.undo_deadline,
            is_undone=reversible_action.is_undone,
            created_at=reversible_action.created_at,
        )


